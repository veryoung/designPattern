/**
 * 首先是原则 solid
 * S – Single Responsibility Principle 单一职责原则
 *      一个类或者一个函数只负责一项职责，不要负责多个职责 
 * O – Open/Closed Principle 开放关闭原则
 *      一个类或者一个函数应该对扩展开放，对修改关闭
 * L – Liskov Substitution Principle 类型替换原则
 *      一个类或者一个函数应该可以使用其他类的对象
 * I – Interface Segregation Principle 接口隔离原则
 *     一个类或者一个函数应该只使用其需要的接口
 * D – Dependency Inversion Principle 依赖倒置原则
 *     一个类或者一个函数应该依赖于抽象而不依赖于具体
 */
